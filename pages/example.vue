			</dl>
			<aside>
				<p>Minions don’t have Resolve but the GM has a pool of Hubris points to spend during the session.</p>
				<p>Each PC has a Hubris rating, which is gained through misdeeds or excessive pride. At the start of the session the GM has a number of Hubris points equal to the total Hubris of the party. In this group Medon has 2 Hubris while Hierax and Xanthippe have 0, so the GM starts with 2 points. The GM can spend Hubris to gain a success on a skill check or activate item properties or powerful NPC special abilities, in a similar manner to Resolve.</p>
