				<dt>Paul (<em>as Hierax</em>):</dt><dd>Can I try and find an easier path up the mountain?</dd>
				<dt>GM:</dt><dd>Sure, give me an Insight (Survival: Tracking) roll to search for an easier route.</dd>
			</dl>
			<aside>
				<p>Insight is the characteristic related to observation and knowledge, while Survival is the skill for wilderness activities such as hunting, tracking, and navigation. Tracking is a specialisation of the Survival skill which can add a bonus to the skill check.</p>
				<p>Paul takes a number of 10-sided dice equal to Hierax’s Insight characteristic plus his Survival skill. Unfortunately, Hierax doesn’t have the Tracking specialisation, so he can’t get a bonus for that. Paul rolls 4D10 and counts every die which comes up 8 or higher. Usually, only one success is needed to succeed at a task but the more successes you have the better your achievement.</p>
			</aside>
			<dl>
				<dt>Paul:</dt><dd>I’ve got a 5, 6, 3, and an 8, so one success.</dd>
				<dt>GM:</dt><dd>After some searching you find a worn path used by goat- herders. It will take you a little longer to reach your destination but you’ll be better rested when you get there. Everyone make a Might (Vigour) roll with +1D from Hierax’s success.</dd>
			</dl>
			<aside>
				<p>Might is the characteristic related to strength and determination, while Vigour is the skill for stamina, fortitude, and discipline.</p>
			</aside>
			<dl>
				<dt>Mike (<em>as Xanthippe</em>):</dt><dd>Two successes.</dd>
				<dt>Sarah (<em>as Medon</em>):</dt><dd>Three successes.</dd>
				<dt>Paul:</dt><dd>Ah, no successes.</dd>
				<dt>GM:</dt><dd>Paul, while gazing up at the mountain ahead of you, you stumble on a loose stone in the path. Gain 1 Risk.</dd>
