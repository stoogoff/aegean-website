			</dl>
			<aside>
				<p>Reach is the effective attacking range of a melee weapon. Most one handed weapons, such as swords, axes, and clubs, have a Reach of 2. When moving to attack someone, the character with the lowest Reach takes a penalty equal to the difference in Reach ratings.</p>
			</aside>
