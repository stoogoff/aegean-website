			<p>It’s often easier to understand how a game works if you can see it played. Below is a transcript from a game session so you can see the rules in play along with the flavour of the game.</p>
			<p>In this session Paul is playing Hierax, a mortal hunter favoured by Artemis. Sarah is playing Medon, a wrestler and son of the war god Ares. Mike is playing Xanthippe, a mortal soldier. All three are citizens of Vlokis.</p>
