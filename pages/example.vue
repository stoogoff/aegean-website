			<aside>
				<p>Risk is a measure of the danger a character is in and is usually gained when a character is harmed in some way, whether physically or mentally. It’s easy to remove and doesn’t cause problems when it’s low but if it gets too high—above the character’s Endurance attribute—the character starts taking Wounds instead of Risk.</p>
			</aside>
