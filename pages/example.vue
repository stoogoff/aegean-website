			<aside>
				<p>Rolling a 10 counts as a success with an additional benefit. Whoever rolled can choose to gain an additional success, give a success to another character making the same roll, remove 1 Risk, or gain 1 Resolve. Resolve can be spent on a future roll to add a success.</p>
			</aside>
